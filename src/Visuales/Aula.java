    private void inicializarTabla() {
        modeloTabla = (DefaultTableModel) Tabla_Aulas.getModel();
        modeloTabla.setRowCount(0);
    }

    /**
     * Carga todas las aulas en la tabla
     */
    private void cargarAulas() {
        if (campus == null) {
            JOptionPane.showMessageDialog(this, "Error: No hay conexión con el campus");
            return;
        }

        modeloTabla.setRowCount(0);

        try {
            List<Clases.Aula> aulas = campus.listarTodasLasAulas();
            System.out.println("Aulas encontradas en cargarAulas: " + aulas.size());

            if (aulas.isEmpty()) {
                System.out.println("No se encontraron aulas");
                return;
            }

            for (Clases.Aula aula : aulas) {
                Object[] fila = {
                        aula.getCodigo(),
                        aula.getTipo(),
                        aula.getNivel(),
                        aula.getEdificio().getNombre()
                };
                modeloTabla.addRow(fila);
                System.out.println("  - Aula agregada: " + aula.getCodigo());
            }

            System.out.println("Total de filas en tabla: " + modeloTabla.getRowCount());

        } catch (Exception e) {
            System.out.println("Error al cargar aulas: " + e.getMessage());
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar aulas: " + e.getMessage());
        }
    }

    private void actualizarTabla() {
        cargarAulas();
    }

    /**
 * Muestra un diálogo para buscar aulas por código parcial
 */
private void buscarAulasPorCodigoParcial() {
    // Crear un panel personalizado para la búsqueda
    javax.swing.JPanel panel = new javax.swing.JPanel();
    panel.setLayout(new java.awt.GridLayout(2, 1, 5, 5));
    
    // Etiqueta instructiva
    javax.swing.JLabel label = new javax.swing.JLabel("Ingrese parte del código del aula a buscar:");
    label.setFont(new java.awt.Font("Tahoma", java.awt.Font.PLAIN, 14));
    panel.add(label);
    
    // Campo de texto para el código
    javax.swing.JTextField txtCodigo = new javax.swing.JTextField(20);
    txtCodigo.setFont(new java.awt.Font("Tahoma", java.awt.Font.PLAIN, 14));
    panel.add(txtCodigo);
    
    // Mostrar el diálogo
    int resultado = JOptionPane.showConfirmDialog(
            this,
            panel,
            "🔍 Buscar Aulas",
            JOptionPane.OK_CANCEL_OPTION,
            JOptionPane.PLAIN_MESSAGE
    );
    
    // Si el usuario presionó OK
    if (resultado == JOptionPane.OK_OPTION) {
        String codigoBuscar = txtCodigo.getText().trim();
        
        // Validar que no esté vacío
        if (codigoBuscar.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                    "Por favor, ingrese un código de aula.", 
                    "Campo vacío", 
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Obtener todas las aulas
        List<Clases.Aula> todasLasAulas = campus.listarTodasLasAulas();
        
        // Limpiar la tabla
        modeloTabla.setRowCount(0);
        
        // Filtrar aulas que contengan el código buscado (IGNORANDO MAYÚSCULAS/MINÚSCULAS)
        int contador = 0;
        for (Clases.Aula aula : todasLasAulas) {
            // Usar toLowerCase() en ambas cadenas para ignorar mayúsculas/minúsculas
            if (aula.getCodigo().toLowerCase().contains(codigoBuscar.toLowerCase())) {
                Object[] fila = {
                        aula.getCodigo(),
                        aula.getTipo(),
                        aula.getNivel(),
                        aula.getEdificio().getNombre()
                };
                modeloTabla.addRow(fila);
                contador++;
            }
        }
        
        if (contador > 0) {
            JOptionPane.showMessageDialog(this, 
                    "✅ Se encontraron " + contador + " aula(s) con el código que contiene: " + codigoBuscar, 
                    "Éxito", 
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, 
                    "❌ No se encontraron aulas con el código que contiene: " + codigoBuscar, 
                    "No encontrado", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }
}
    
    /**
 * Muestra un diálogo para buscar un aula por código
 */
private void buscarAulaPorCodigo() {
    // Crear un panel personalizado para la búsqueda
    javax.swing.JPanel panel = new javax.swing.JPanel();
    panel.setLayout(new java.awt.GridLayout(2, 1, 5, 5));
    
    // Etiqueta instructiva
    javax.swing.JLabel label = new javax.swing.JLabel("Ingrese el código del aula a buscar:");
    label.setFont(new java.awt.Font("Tahoma", java.awt.Font.PLAIN, 14));
    panel.add(label);
    
    // Campo de texto para el código
    javax.swing.JTextField txtCodigo = new javax.swing.JTextField(20);
    txtCodigo.setFont(new java.awt.Font("Tahoma", java.awt.Font.PLAIN, 14));
    panel.add(txtCodigo);
    
    // Mostrar el diálogo
    int resultado = JOptionPane.showConfirmDialog(
            this,
            panel,
            "🔍 Buscar Aula",
            JOptionPane.OK_CANCEL_OPTION,
            JOptionPane.PLAIN_MESSAGE
    );
    
    // Si el usuario presionó OK
    if (resultado == JOptionPane.OK_OPTION) {
        String codigo = txtCodigo.getText().trim();
        
        // Validar que no esté vacío
        if (codigo.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                    "Por favor, ingrese un código de aula.", 
                    "Campo vacío", 
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Buscar el aula 
        Clases.Aula aulaEncontrada = null;
        List<Clases.Aula> todasLasAulas = campus.listarTodasLasAulas();
        
        // Recorrer todas las aulas y comparar 
        for (Clases.Aula aula : todasLasAulas) {
            if (aula.getCodigo().equalsIgnoreCase(codigo)) {
                aulaEncontrada = aula;
                break;
            }
        }
        
        // Limpiar la tabla
        modeloTabla.setRowCount(0);
        
        if (aulaEncontrada != null) {
            // Agregar el aula encontrada a la tabla
            Object[] fila = {
                    aulaEncontrada.getCodigo(),
                    aulaEncontrada.getTipo(),
                    aulaEncontrada.getNivel(),
                    aulaEncontrada.getEdificio().getNombre()
            };
            modeloTabla.addRow(fila);
            
            JOptionPane.showMessageDialog(this, 
                    "✅ Aula encontrada: " + codigo, 
                    "Éxito", 
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, 
                    "❌ No se encontró un aula con el código: " + codigo, 
                    "No encontrado", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }
}
    

