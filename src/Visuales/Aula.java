    private void Agg_AulaActionPerformed(java.awt.event.ActionEvent evt) {
        String codigo = JOptionPane.showInputDialog(this, "Ingrese el código del aula:");
        if (codigo == null || codigo.trim().isEmpty()) return;

        // Verificar si el aula ya existe
        Clases.Aula existente = campus.buscarAula(codigo.trim());
        if (existente != null) {
            JOptionPane.showMessageDialog(this, "Error: Ya existe un aula con el código " + codigo.trim());
            return;
        }

        // Obtener lista de edificios
        List<Edificio> edificios = campus.listarEdificios();
        if (edificios.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay edificios disponibles. Cree un edificio primero.");
            return;
        }

        // Seleccionar edificio
        String[] nombresEdificios = edificios.stream()
                .map(Edificio::getNombre)
                .toArray(String[]::new);

        String edificioSeleccionado = (String) JOptionPane.showInputDialog(
                this,
                "Seleccione el edificio:",
                "Edificio",
                JOptionPane.QUESTION_MESSAGE,
                null,
                nombresEdificios,
                nombresEdificios[0]
        );

        if (edificioSeleccionado == null) return;

        Edificio edificio = campus.buscarEdificioPorNombre(edificioSeleccionado);
        if (edificio == null) {
            JOptionPane.showMessageDialog(this, "Error: Edificio no encontrado");
            return;
        }

        String nivel = JOptionPane.showInputDialog(this, "Ingrese el nivel (ej: 1, 2, 3):");
        if (nivel == null || nivel.trim().isEmpty()) return;

        String tipo = (String) JOptionPane.showInputDialog(
                this,
                "Seleccione el tipo de aula:",
                "Tipo",
                JOptionPane.QUESTION_MESSAGE,
                null,
                new String[]{"Teoria", "Laboratorio", "Seminario"},
                "Teoria"
        );

        if (tipo == null) return;

        // Crear nueva aula
        Clases.Aula nuevaAula = new Clases.Aula(codigo.trim(), edificio, nivel.trim(), tipo);

        try {
            if (campus.agregarAula(nuevaAula)) {
                JOptionPane.showMessageDialog(this, "Aula agregada correctamente");
                actualizarTabla();
            } else {
                JOptionPane.showMessageDialog(this,
                        "Error: No se pudo agregar el aula.\n" +
                                "Verifique que el nivel '" + nivel + "' pueda crearse en el edificio.",
                        "Error al agregar aula",
                        JOptionPane.ERROR_MESSAGE
                );
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error inesperado: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
            e.printStackTrace();
        }
    }

